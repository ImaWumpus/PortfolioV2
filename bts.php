    <br>
    En plus des matières en lien avec l’ingénierie informatique, la formation propose également des enseignements de culture générale, d’anglais (avec la possibilité de continuer une LV2), de mathématiques appliquées à l’informatique, ainsi que sur les outils d’analyse économique, managériale et juridique des services informatiques.<br> 
    <br>
    De plus, au cours de cette formation, il est possible de bénéficier d’un enseignement approfondi en mathématiques et de travailler en autonomie en laboratoire informatique.<br>
